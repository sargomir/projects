<p><strong>Раздел Задачи</strong></p>
<p><strong>&nbsp;</strong></p>
<ul>
<li>Постановка задачи</li>
</ul>
<p>&laquo;ГИП&raquo; создает новое &laquo;Задание&raquo; в разделе &laquo;Задачи&raquo;, указывает &laquo;Проект&raquo; и &laquo;Элемент проекта&raquo;, формулирует &laquo;Задачу&raquo;, &laquo;Результат&raquo;, &laquo;Расчетные трудозатраты&raquo; на ее выполнение, определяет &laquo;Сотрудника&raquo;, которому задача будет назначена. Кроме &laquo;ГИПа&raquo; &laquo;Задачу&raquo; может назначить &laquo;Менеджер проектов&raquo;</p>
<p>&nbsp;</p>
<p>При формулировании задачи необходимо руководствоваться критериями SMART:</p>
<ul>
<li>Specific &mdash; конкретная, определённая. Задача &laquo;откорректировать документацию&raquo; не является чёткой, альтернативой будет &laquo;откорректировать документ №ххх по замечаниям, изложенным в xxx&raquo;.</li>
<li>Measurable &mdash; измеримая. Задача должна подразумевать количественную измеримость результата. Пример &laquo;Должно быть откорректировано n документов&raquo;.</li>
<li>Achievable &mdash; достижимая. Задача должна быть выполнимой для конкретного исполнителя.</li>
<li>Relevant &mdash; соответствующая контексту. Выполнение задачи должно быть обеспечено ресурсами.</li>
<li>Timed/Time-bounded &mdash; привязанная к точке (начала или окончания) и продолжительности времени необходимому для выполнения задачи. Нет привязки &mdash; нет задачи (есть мечты)!</li>
</ul>
<p>Аналогичным образом формулируется и результат.</p>
<p>Формулировка задачи по тендерам должна начинаться с номера тендера!</p>
<p>&nbsp;</p>
<p>&laquo;ГИП&raquo; и &laquo;Менеджер проектов&raquo; <strong>может отредактировать задачу в течении 24 часов</strong> с момента создания, если &laquo;Сотрудник&raquo; не установил флаг &laquo;Выполнено&raquo; или не изменил &laquo;Фактические трудозатраты&raquo;.</p>
<p>&nbsp;</p>
<p>При указании трудозатрат и сроков сдачи работы необходимо учитывать следующее:</p>
<ul>
<li>На выполнение &laquo;Задачи&raquo; отводится срок не далее конца месяца. Если &laquo;Задача&raquo; не будет принята и отмечена как &laquo;Выполненная&raquo; до 5 числа следующего месяца от даты ее создания, она останется невыполненной, ее редактирование будет запрещено. Так же как и создание новых задач в прошлом месяце.</li>
<li>Если задача не может быть выполнена в течении месяца, то есть два варианта выхода из ситуации:
<ul>
<li>Разделить задачу на более мелкие подзадачи и в текущем месяце закрыть только часть.</li>
<li>Разделить задачу на две и более по времени, закрыв в текущем месяце часть в часах пропорционально реальному выполнению задачи. Для этого требуется экспертная оценка состояния работ &laquo;Техническим руководителем&raquo; и &laquo;ГИПом&raquo;</li>
</ul>
</li>
<li>Выполнение задачи</li>
</ul>
<p>&laquo;Сотрудник&raquo;, которому назначена &laquo;Задача&raquo;, может ее редактировать по ходу выполнения, указывая &laquo;Фактические трудозатраты&raquo;. По выполнении задачи &laquo;Сотрудник&raquo; фиксирует флаг &laquo;Выполнено&raquo;.</p>
<p><strong>Надо иметь в виду, что для целей корректировки премиальной части зарплаты будут учитываться только расчетные трудозатраты.</strong></p>
<p>Фактическое время будет использоваться исключительно для анализа деятельности сотрудников и их эффективности и перспективности.</p>
<p><strong>У &laquo;Заданий&raquo; предусмотрена система комментариев, посредством которой можно комментировать процесс выполнения задания, конкретизации задания или для фиксации временных вех. </strong></p>
<ul>
<li>Принятие результатов</li>
<li>&laquo;Технический руководитель&raquo; принимает результат задачи с технической и нормативной точки зрения и фиксирует флаг &laquo;Выполнено&raquo;.</li>
<li>&laquo;ГИП&raquo; принимает результат задачи с точки зрения соответствия проектным задачам и фиксирует флаг &laquo;Выполнено&raquo;.</li>
<li>&laquo;Менеджер проектов&raquo; принимает &laquo;Задачу&raquo; с точки зрения соблюдения сроков и бюджета и фиксирует флаг &laquo;Выполнено&raquo;.</li>
</ul>
<p>&nbsp;</p>
<p>По завершении всех этих шагов &laquo;Задача&raquo; считается успешно завершенной. &laquo;Задача&raquo; считается невыполненной, если отсутствует хотя бы один из флагов &laquo;Выполнено&raquo;.</p>
<p><strong>Только завершенные задачи попадут в сводную ведомость трудозатрат для предоставления в бухгалтерию.</strong></p>
<p><strong>&nbsp;</strong></p>
<p><strong>&nbsp;</strong></p>
<p><strong>Раздел Пользователи</strong></p>
<p>&nbsp;</p>
<p>Данный раздел доступен и ведется специалистом по кадрам.</p>
<p>В базу пользователей вносятся действующие сотрудники компании. У каждого сотрудника есть своя роль, у некоторых она не одна.</p>
<p>В системе существует пять ролей пользователей:</p>
<ul>
<li>Сотрудник</li>
<li>Технический руководитель (согласно Функциональной схеме компании)</li>
<li>ГИП (назначается Приказом по проекту)</li>
<li>Менеджер проектов</li>
<li>Управление пользователями (администрация)</li>
</ul>
<p>Каждый пользователь входит в систему под своим паролем, который был присвоен посредством генератора случайных чисел и передан пользователю лично.</p>
<p>В случае увольнения сотрудника, все его задачи должны быть закрыты, после чего делается отметка об увольнении в разделе Пользователи.</p>
