<p><strong>Инструкция по пользованию системой учета рабочего времени.</strong></p>
<p>&nbsp;</p>
<p>Система предназначена для автоматизированного учета рабочего времени сотрудников посредством выдачи заданий на работу и отслеживания их выполнения.</p>
<p>В системе должны отчитываться только те сотрудники, деятельность которых распределена между проектами, а именно:</p>
<ul>
<li>Инженерный состав</li>
<li>Производственный состав</li>
</ul>
<p>&nbsp;</p>
<p>В интерфейсе системы присутствует пять разделов:</p>
<ul>
<li>Справка</li>
<li>Проекты</li>
<li>Бюджеты</li>
<li>Задачи</li>
<li>Пользователи</li>
</ul>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>Раздел Проекты</strong></p>
<p>&nbsp;</p>
<p>В данном разделе представлен список действующих проектов.</p>
<p>Для создания нового проекта &laquo;Менеджер проектов&raquo; выбирает кнопку &laquo;Добавить&raquo; и в открывшейся форме заполняет следующие поля:</p>
<ol>
<li>Проект.</li>
</ol>
<p>Название нового проекта заводится в определенном формате.</p>
<ul>
<li>для действующих проектов:</li>
</ul>
<p>Rxxx_Сокращенно заказчик_Сокращенно наименование_Год</p>
<ul>
<li>для тендерных проектов:</li>
</ul>
<p>Txxx_Сокращенно заказчик_Сокращенно наименование_Год</p>
<p>В случае выигрыша номер тендера меняется на номер проекта, при этом номер тендера переносится в комментарии проекта.</p>
<ul>
<li>для внутренних проектов:</li>
</ul>
<p>Exxx.Год_Сокращенно наименование</p>
<ul>
<li>для НИИР проектов:</li>
</ul>
<p>Xxxx_ Сокращенно наименование_Год</p>
<ol start="2">
<li>Заказчик.</li>
</ol>
<p>Краткое название компании &ndash; для стороннего Заказчика;</p>
<p>Собственные средства &ndash; для внутренних проектов;</p>
<ol start="3">
<li>ГИП (руководитель проекта выбирается из списка);</li>
<li>Активные (выставляется соответствующий флаг: Да / Нет),</li>
</ol>
<p>&nbsp;</p>
<p>Далее сохраняем внесенные данные, нажатием кнопки с изображением дискеты. При необходимости можно добавить комментарии к проекту (в режиме просмотра, нажав на значок &laquo;глаз&raquo;) или отредактировать ранее внесенные данные (нажав на значок &laquo;карандаш&raquo;).</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong>Раздел Бюджеты</strong></p>
<p>&nbsp;</p>
<p>Любому элементу проекта менеджер проектов может назначить расчетный бюджет.</p>
<p>Для внесения &laquo;расчетных трудозатрат&raquo; по проекту необходимо перейти в раздел &laquo;Бюджеты&raquo;. Нажатием кнопки &laquo;Новый бюджет&raquo; открыть соответствующую форму и заполнить поля:</p>
<ol>
<li>Проект (выбрать из списка)</li>
<li>Элемент проекта.</li>
</ol>
<ul>
<li>Для действующих проектов выбрать 1 из 5 элементов: [ПИР], [Обор], [СМР], [ПНР], [Общее]</li>
<li>Для всех тендеров должен указываться [ТКП]</li>
<li>Для внутренних проектов &ndash; [Общее]</li>
</ul>
<ol start="3">
<li>Бюджет.</li>
</ol>
<ul>
<li>Для действующих проектов трудозатраты берутся из утвержденного бюджета.</li>
<li>Для тендерных проектов трудозатраты определяются менеджером проектов и ограничены сроком подачи заявки по тендеру.</li>
<li>Для внутренних проектов:</li>
</ul>
<ul>
<li>E001-E004 Общее руководство отделом. Руководителю выделяется 7% суммарного объема времени подразделения без учета руководителя.</li>
<li>E005 Общехозяйственные работы по компании. Выделяется по усмотрению руководителя компании.</li>
<li>E006 Общехозяйственные работы по производству. Выделяется 5% суммарного объема времени подразделения без учета руководителя.</li>
<li>E007-E008 Общехозяйственные работы по филиалам. Выделяется по 24 ч в месяц</li>
<li>E009 Больничный.</li>
<li>E010 Отпуск. Согласно положениям компании.</li>
<li>X*** НИИР проекты. Трудозатраты берутся из утвержденного бюджета НИИР проекта.</li>
</ul>
<p>Далее сохранить внесенные данные. При необходимости данные можно откорректировать.</p>
<p>За актуальность информации по разделу отвечает менеджер проектов.</p>
